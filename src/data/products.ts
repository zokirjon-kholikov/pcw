const usd = (n: number) => `$${n.toLocaleString('en-US')}`;

const storage = (gb: number) =>
  gb >= 1024 ? `${(gb / 1024).toLocaleString('en-US')} TB` : `${gb} GB`;

const weight = (g: number) =>
  g >= 1000 ? `${(g / 1000).toFixed(2)} kg` : `${g} g`;

const measure = (raw: string) => {
  const match = raw.match(/^([\d.,]+)\s*(.*)$/);
  if (!match) return null;
  return { num: Number(match[1].replace(/,/g, '')), unit: match[2].trim() };
};

